# las tuplas son muy parecidas a las listas 
# y nos permiten almacenar diferentes tipos de datos
# strings, enteros, flotantes, booleanos

# La tupla nos servirá principalmente para consultar valores
#ya no podremos añadir o quitar elementod de ella y mucho 
# menos modificar los elementos que se encuentran almacenados
# la ventaja de usar tuplas, es que son mucho más rápidas
# en cuanto a obtener elementos se refiere
# yA QUE LAS TUPLAS AL SER ELEMENTOS inmutables
# Python las almacena en un espacio de lectura y por ello es más fácil acceder a ellas

#            0      1   2      3        4          5
tupla = ( 'Sting', 10, 15.4, True, [1, 2, 3], (4, 5, 6) )

