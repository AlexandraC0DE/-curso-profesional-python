name = 'Alexandra Mariela'
last_name = 'García'

# Por default la función print va a separar los valores mediante un espacio 
# sin embargo se pueden separar los valores utilizando otros caractares
# utilizando sep='aquí va un caracter especial'
# el parámetro sep se coloca al final de la función print 
# y sirve para separar los string con caracteres especiales como -, @, //, etc.
print(name, last_name,'González', sep='-')