# Python tiene un tipo de dato básico llamado none
# none es usado para definir un valor vacío
# frecuentemente usaremos none para representar la ausencia de algún valor
# utilizando none seremos capaces de crear variables que simplemente no almacenen valor alguno
# algo muy útil cuando aún no sabemos que tipo de valor usaremos más adelante

result = None
# más adelante podrás asignarle un valor a la variable ej. Un listado de númeors enteros 
result = [1, 2, 3]
print(result)
print(type(result))

# Consejo: siempre que vamos a trabajar con algín tipo de valor peor no sepamos su tipo
# lo mejor es evitar crear variables de diferentes tipos
# y unicamente creemos una variable que no almacene valor alguno
# de esta forma evitaremos usar espacio en memoria que muy probablemente no vayamos a utilizar