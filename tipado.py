# Para conocer que tipo de dato está almacenando una variable
# podemos hacer uso de la función type()
value = 'Alexandra'
type_data = type(value)
print(type_data)

value = 2
type_data = type(value)
print(type_data)

value = 3.1
type_data = type(value)
print(type_data)

value = True
type_data = type(value)
print(type_data)


